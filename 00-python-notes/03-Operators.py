# Comparison Operators
# ==, !=, >, <, >=, <=

#Logical Operators
# and, or, not
a = True
b = False
print('a and b is', a and b)
print('a or b is', a or b)
print('not a is', not a)

#Bitwise operators
# &, |, ~, ^, >>, <<

#Special operators

# Identity Operators
# is, is not
a = 5
b = 5
print(a is b)

l1 = [1, 2, 3]
l2 = [1, 2, 3]
print(l1 is l2)

# Membership Operators
# in, not in
x = 'Hello world'
print('H' in x)
print('h' not in x)
print('Hello' not in x)
print('Hello' in x)

# Ternary Operator
a, b = 10, 20
min = a if a < b else b
print(min)
# Output: 10

# Chaining comparison operators
x = 5
print(1 < x < 10)
# Output: True

print(10 < x < 20)
# Output: False
print(5 == x < 10)
# Output: True
print(5 == x > 10)
# Output: False

